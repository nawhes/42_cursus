#include "libft.h"

int			next(char *line, int *i)
{
	if (*(line + *i) != ' ' && *(line + *i) != ',')
		return (-1);
	while (*(line + *i) == ' ' || *(line + *i) == ',')
		(*i)++;
	return (0);
}
